def normalize_spaces(text: str) -> str:
    text = text.replace("（ ", "（").replace(" ）", "）")
    text = re.sub(r"\s+", " ", text)
    return text.strip()


def apply_global_replacements(text: str) -> str:
    for before, after in GLOBAL_REPLACEMENTS.items():
        text = text.replace(before, after)
    return text


def clean_prompt(text: str, blank_label: str) -> str:
    text = apply_global_replacements(text)
    text = re.sub(r"^\d+\s*", "", text)
    text = text.replace("（ ）", "（　）").replace("( )", "（　）")
    text = text.replace("（（ ）", "（　）").replace("（（", "（　）")
    text = re.sub(
        rf"[「『【\[]?{blank_label}[」』】\]]?(?=[のにをでとはが、。,.%度cｍmM系色芝台壁床音域万テ図仕場光階線サ以未])",
        "（　）",
        text,
    )
    text = re.sub(
        rf"(?<=\s){blank_label}(?=[のにをでとはが、。,.%度cｍmM系色芝台壁床音域万テ図仕場光階線サ以未])",
        "（　）",
        text,
    )
    text = re.sub(r"([。、])\s*", r"\1", text)
    text = re.sub(r"([、。])(?=[^ \n])", r"\1", text)
    text = re.sub(r"([ぁ-んァ-ン一-龠])\s+([ぁ-んァ-ン一-龠])", r"\1\2", text)
    text = text.replace("。支払い条件", "、支払い条件")
    text = text.replace("、（　）保証", "、（　）保証")
    text = text.replace("（　）がかって", "（　）みがかって")
    text = text.replace("（　）音域", "（　）音域")
    return normalize_spaces(text)


def clean_value(text: str) -> str:
    if text is None:
        return text
    text = apply_global_replacements(text)
    text = text.replace("〜", "〜")
    text = text.replace("（にかわ）", "（にかわ）")
    return normalize_spaces(text)


def main() -> int:
    payload = json.loads(QUESTIONS_PATH.read_text(encoding="utf-8"))
    questions = payload["questions"]

    for source_id, duplicate_id in DUPLICATE_OVERRIDES.items():
        if source_id in PROMPT_OVERRIDES:
            PROMPT_OVERRIDES[duplicate_id] = dict(PROMPT_OVERRIDES[source_id])

    for question in questions:
        question["prompt"] = clean_prompt(question["prompt"], question["blank_label"])
        question["options"] = [clean_value(option) for option in question["options"]]
        if question.get("answer_text") is not None:
            question["answer_text"] = clean_value(question["answer_text"])

        override = PROMPT_OVERRIDES.get(question["id"])
        if override:
            if "prompt" in override:
                question["prompt"] = override["prompt"]
            if "options" in override:
                question["options"] = override["options"]
            if "answer_index" in override:
                question["answer_index"] = override["answer_index"]

        if question.get("answer_index") is not None and question["answer_index"] < len(question["options"]):
            question["answer_text"] = question["options"][question["answer_index"]]

    QUESTIONS_PATH.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    print(f"updated {len(questions)} questions")
    return 0


